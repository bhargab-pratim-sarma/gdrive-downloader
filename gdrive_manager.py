# ─── Show collection stats ─────────────────────────────────────────────────────
def cmd_stats():
    """Show collection stats: file counts and total size by type."""
    print(bold(clr("\n── COLLECTION STATS ────────────────────────────────────", C.CYAN)))
    if not COLLECTION.exists():
        print(err(f"Collection folder not found: {COLLECTION}"))
        return

    ext_counts = {}
    ext_sizes  = {}
    part_count = 0
    total_size = 0

    for p in COLLECTION.rglob("*"):
        if p.is_file():
            if p.suffix == ".part":
                part_count += 1
                continue
            ext   = p.suffix.lower() or "(no ext)"
            size  = p.stat().st_size
            ext_counts[ext] = ext_counts.get(ext, 0) + 1
            ext_sizes[ext]  = ext_sizes.get(ext, 0) + size
            total_size      += size

    total_files = sum(ext_counts.values())
    print(f"  {'Extension':<15} {'Files':>6}  {'Size':>10}")
    print(f"  {'─'*15}  {'─'*6}  {'─'*10}")
    for ext in sorted(ext_counts, key=lambda e: -ext_sizes.get(e, 0)):
        size_mb = ext_sizes.get(ext, 0) / (1024**2)
        ext_label = clr(f"{ext:<15}", C.CYAN)
        print(f"  {ext_label} {ext_counts[ext]:>6}  {size_mb:>9.1f}M")
    total_gb = total_size / (1024**3)
    print(f"\n  {'TOTAL':<15} {total_files:>6}  {total_gb:>9.2f}G")
    if part_count:
        print(warn(f"\n  {part_count} .part file(s) found — run option 3 to fix."))

# ─── Refresh Manifest (re-scan Drive) ────────────────────────────────────────
def cmd_refresh_manifest():
    """
    Re-scan the Google Drive folder and rebuild gdown_full_list.txt.
    Uses: gdown --folder --remaining-ok <URL> (dry listing via --no-download)
    Since gdown doesn't have a pure list mode, we capture the verbose output
    by running gdown in a temp dir and capturing its stdout.
    """
    print(bold(clr("\n── REFRESH DRIVE MANIFEST ──────────────────────────────", C.CYAN)))
    print(info(f"Drive URL: {DRIVE_URL}"))
    print(info("Scanning Google Drive folder structure… (this may take 1-2 minutes)"))
    print(warn("Do NOT interrupt — let it complete for an accurate manifest.\n"))

    # Backup old manifest
    backup = None
    if MANIFEST.exists():
        backup = BASE / f"gdown_full_list.backup_{datetime.now().strftime('%Y%m%d_%H%M%S')}.txt"
        MANIFEST.rename(backup)
        print(info(f"Old manifest backed up to: {backup.name}"))

    # Run gdown folder scan — capture output to rebuild manifest
    # gdown writes its scan log to stdout when run with --folder
    cmd = [
        sys.executable, "-m", "gdown",
        "--folder", DRIVE_URL,
        "--remaining-ok",
        "--no-download",  # only list, don't actually download
        "-O", str(COLLECTION),
    ]

    # Fallback: gdown may not support --no-download for folder mode
    # So we use the subprocess approach capturing the listing output
    cmd_list = [
        sys.executable, "-c",
        f"""
import sys, gdown
from io import StringIO
import contextlib

output = []
original_print = print
def capture_print(*args, **kwargs):
    output.append(' '.join(str(a) for a in args))
    original_print(*args, **kwargs)

import builtins
builtins.print = capture_print
try:
    gdown.download_folder(
        url='{DRIVE_URL}',
        output='{str(COLLECTION)}',
        quiet=False,
        remaining_ok=True,
        skip_download=True,
    )
except Exception as e:
    original_print(f'Note: {{e}}')
finally:
    builtins.print = original_print

with open('{str(MANIFEST)}', 'w') as f:
    f.write('\\n'.join(output))
original_print('DONE')
"""
    ]

    print(info("Attempting Drive scan via gdown.download_folder …"))
    try:
        result = subprocess.run(
            cmd_list, capture_output=False, text=True, timeout=300
        )
        if MANIFEST.exists() and MANIFEST.stat().st_size > 100:
            lines = sum(1 for _ in open(MANIFEST))
            print(ok(f"Manifest refreshed: {lines} entries written to {MANIFEST.name}"))
            if backup:
                print(info(f"Backup kept at: {backup.name} (delete manually if not needed)"))
        else:
            # Try classic gdown CLI approach as fallback
            print(warn("First attempt incomplete — trying CLI fallback …"))
            _refresh_via_cli(backup)
    except subprocess.TimeoutExpired:
        print(err("Scan timed out after 5 minutes."))
        _restore_backup(backup)
    except Exception as e:
        print(err(f"Scan failed: {e}"))
        _restore_backup(backup)

def _refresh_via_cli(backup):
    """Fallback: run gdown CLI and capture output to file."""
    cmd = [
        "gdown", "--folder", DRIVE_URL,
        "--remaining-ok", "-O", str(COLLECTION)
    ]
    print(info("Running: " + " ".join(cmd)))
    print(warn("This WILL start downloading — press Ctrl+C after the file listing appears."))
    try:
        with open(MANIFEST, "w") as out_f:
            proc = subprocess.Popen(
                cmd, stdout=subprocess.PIPE, stderr=subprocess.STDOUT,
                text=True, bufsize=1
            )
            for line in proc.stdout:
                sys.stdout.write(line)
                out_f.write(line)
            proc.wait()
        lines = sum(1 for _ in open(MANIFEST))
        print(ok(f"Manifest saved: {lines} lines → {MANIFEST.name}"))
    except KeyboardInterrupt:
        print(warn("\nInterrupted — partial manifest saved. Run Verify to check coverage."))
    except FileNotFoundError:
        print(err("gdown CLI not found. Run option [0] to install dependencies."))
        _restore_backup(backup)

def _restore_backup(backup):
    if backup and backup.exists():
        backup.rename(MANIFEST)
        print(warn(f"Restored previous manifest from backup."))

