def main():
    print_header()

    # Quick dependency check
    if not GDOWN_AVAILABLE or not TQDM_AVAILABLE:
        missing_deps = []
        if not GDOWN_AVAILABLE: missing_deps.append("gdown")
        if not TQDM_AVAILABLE:  missing_deps.append("tqdm")
        print(warn(f"Missing packages: {', '.join(missing_deps)}"))
        print(info("Run option [0] to install them automatically.\n"))

    actions = {key: fn for key, _, fn in MENU}

    while True:
        print_menu()
        try:
            choice = input(clr("  Enter choice: ", C.WHITE)).strip().lower()
        except EOFError:
            break

        if choice == "q":
            print(clr("\n  Goodbye!\n", C.CYAN))
            break
        elif choice in actions and actions[choice]:
            try:
                actions[choice]()
            except KeyboardInterrupt:
                print(warn("\nOperation cancelled by user."))
        else:
            print(warn("Invalid choice. Please try again."))

        input(clr("\n  Press ENTER to return to menu…", C.DIM))

if __name__ == "__main__":
    main()
