# ─── Extension picker ─────────────────────────────────────────────────────────
def pick_extensions() -> set:
    """Interactive menu to select which file types to include/exclude."""
    print(bold(clr("\n── SELECT FILE TYPES ───────────────────────────────────", C.CYAN)))
    print("  Toggle groups ON/OFF. Press ENTER when done.\n")

    active = {"1", "2", "3", "4", "5"}   # all on by default

    while True:
        for key, (label, exts, color) in EXTENSION_GROUPS.items():
            status = clr("ON ", C.GREEN) if key in active else clr("OFF", C.RED)
            ext_str = clr(", ".join(sorted(exts)[:5]), C.DIM)
            print(f"  [{clr(key, C.YELLOW)}] [{status}]  {clr(label, color):20s}  {ext_str}")
        print(f"\n  [{clr('0', C.WHITE)}] Done / Confirm selection")
        print(f"  [{clr('a', C.WHITE)}] All ON  |  [{clr('n', C.WHITE)}] All OFF")

        choice = input(f"\n  Toggle group number: ").strip().lower()
        if choice == "0" or choice == "":
            break
        elif choice == "a":
            active = set(EXTENSION_GROUPS.keys())
        elif choice == "n":
            active = set()
        elif choice in EXTENSION_GROUPS:
            if choice in active:
                active.remove(choice)
            else:
                active.add(choice)
        print()

    # Build the combined skip_ext set (everything NOT active)
    include_ext = set()
    for key, (_, exts, _) in EXTENSION_GROUPS.items():
        if key in active:
            include_ext |= exts

    # We return the extensions to SKIP (inverse of include)
    all_known = set()
    for _, (_, exts, _) in EXTENSION_GROUPS.items():
        all_known |= exts
    skip_ext = all_known - include_ext

    print(f"\n{ok('Including:')} {clr(', '.join(sorted(include_ext)) or '(none)', C.GREEN)}")
    print(f"{warn('Excluding:')} {clr(', '.join(sorted(skip_ext)) or '(none)', C.DIM)}")
    return skip_ext

