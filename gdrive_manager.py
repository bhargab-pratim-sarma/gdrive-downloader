# ─── Filename Utilities ────────────────────────────────────────────────────────
def clean_name(name: str) -> str:
    """Sanitise a filename the same way the original scripts do."""
    name = re.sub(r'Retrieving folder contents completed', '', name)
    name = re.sub(r'Building directory structure.*', '', name)
    return re.sub(r'[^\w\s\-\.\[\]\~]', '_', name).strip()

JUNK_PATTERNS = [
    "Retrieving folder", "Building directory",
    "folder contents completed", "Downloading...",
    "From: https://", "To:", "%|"
]
def is_junk_line(s: str) -> bool:
    return any(p in s for p in JUNK_PATTERNS)

# ─── Manifest Parser ───────────────────────────────────────────────────────────
def parse_manifest(skip_ext: set = None) -> list[dict]:
    """
    Parse gdown_full_list.txt and return a list of file records:
      { id, name, ext, rel_dir, abs_path }
    skip_ext: set of extensions to exclude (e.g. VIDEO_EXTENSIONS).
    """
    if not MANIFEST.exists():
        print(err(f"Manifest not found: {MANIFEST}"))
        return []

    with open(MANIFEST, "r", errors="ignore") as f:
        lines = f.readlines()

    records = []
    current_main = ""
    current_sub  = ""

    for raw in lines:
        line = raw.strip()

        # ── Folder lines ──
        fm = re.search(r'Retrieving folder [a-zA-Z0-9_-]+ (.*)', line)
        if fm:
            fname = fm.group(1).strip()
            if is_junk_line(fname):
                continue
            if fname.startswith('['):
                current_main = clean_name(fname)
                current_sub  = ""
            elif fname.startswith('~') or fname == "":
                current_sub = clean_name(fname)
            else:
                current_sub = clean_name(fname)
            continue

        # ── File lines ──
        pm = re.search(r'Processing file ([a-zA-Z0-9_-]+) (.*)', line)
        if not pm:
            continue

        fid   = pm.group(1)
        fname = pm.group(2).strip()

        if is_junk_line(fname) or not fname:
            continue

        ext = Path(fname).suffix.lower()
        if skip_ext and ext in skip_ext:
            continue

        # Build relative directory from current context
        parts = [str(COLLECTION)]
        if current_main:
            parts.append(current_main)
        if current_sub:
            parts.append(current_sub)

        target_dir  = Path(os.path.join(*parts))
        target_path = target_dir / clean_name(fname)

        records.append({
            "id":       fid,
            "name":     fname,
            "ext":      ext,
            "rel_dir":  str(target_dir.relative_to(COLLECTION)),
            "abs_dir":  str(target_dir),
            "abs_path": str(target_path),
        })

    return records

# ─── Simple progress bar (fallback if tqdm not available) ─────────────────────
def simple_bar(current: int, total: int, width: int = 40, label: str = "") -> str:
    pct   = current / max(total, 1)
    filled = int(width * pct)
    bar   = "█" * filled + "░" * (width - filled)
    return f"\r{clr(bar, C.BLUE)} {pct*100:5.1f}%  {label}"

