# ─── Verification ─────────────────────────────────────────────────────────────
def scan_parts(search_root: Path = None) -> list[str]:
    """Return all .part files under search_root (default: EBOOK base dir)."""
    root = search_root or BASE
    return [str(p) for p in root.rglob("*.part")]

def scan_zero_bytes(records: list[dict]) -> list[dict]:
    """Return records whose downloaded file exists but is 0 bytes."""
    return [r for r in records if Path(r["abs_path"]).exists()
            and Path(r["abs_path"]).stat().st_size == 0]

def scan_missing(records: list[dict]) -> list[dict]:
    """Return records whose target file does not exist."""
    return [r for r in records if not Path(r["abs_path"]).exists()]

def cmd_verify(skip_ext: set = None):
    """Run a full verification scan and print a report."""
    print(bold(clr("\n── VERIFICATION SCAN ──────────────────────────────────", C.CYAN)))
    skip_ext = skip_ext or ALL_VIDEO_EXT
    records  = parse_manifest(skip_ext=skip_ext)
    if not records:
        return

    total   = len(records)
    missing = scan_missing(records)
    zeros   = scan_zero_bytes(records)
    parts   = scan_parts()

    # ── Part files ──
    if parts:
        print(warn(f"Incomplete (.part) files found: {len(parts)}"))
        for p in parts:
            short = str(Path(p).relative_to(BASE))
            print(f"   {clr('•', C.YELLOW)} {short}")
    else:
        print(ok("No .part files found"))

    # ── Zero-byte files ──
    if zeros:
        print(warn(f"Empty (0-byte) files found: {len(zeros)}"))
        for z in zeros:
            print(f"   {clr('•', C.YELLOW)} {z['name']}")
    else:
        print(ok("No zero-byte files found"))

    # ── Missing files ──
    if missing:
        print(err(f"Missing files: {len(missing)} / {total}"))
        for m in missing[:20]:   # cap display at 20
            print(f"   {clr('•', C.RED)} {m['name']}")
        if len(missing) > 20:
            print(clr(f"   … and {len(missing)-20} more", C.DIM))
    else:
        print(ok(f"All {total} expected files are present"))

    # ── Summary ──
    print(clr("\n── SUMMARY ─────────────────────────────────────────────", C.CYAN))
    print(f"  Total expected  : {bold(str(total))}")
    print(f"  Present & OK    : {clr(str(total - len(missing) - len(zeros)), C.GREEN)}")
    print(f"  Missing         : {clr(str(len(missing)), C.RED if missing else C.GREEN)}")
    print(f"  Zero-byte       : {clr(str(len(zeros)), C.RED if zeros else C.GREEN)}")
    print(f"  .part files     : {clr(str(len(parts)), C.RED if parts else C.GREEN)}")

    if not missing and not zeros and not parts:
        print(f"\n{ok('PERFECT — everything is fully downloaded!')}")
    else:
        print(f"\n{warn('Run option 3 (Fix & Re-download) to repair the issues above.')}")

# ─── Fix Broken / Partial Downloads ───────────────────────────────────────────
def cmd_fix(skip_ext: set = None, dry_run: bool = False):
    """
    1. Delete all .part files.
    2. Delete zero-byte files.
    3. Re-download them via the download engine.
    """
    print(bold(clr("\n── FIX & RE-DOWNLOAD ───────────────────────────────────", C.CYAN)))
    skip_ext = skip_ext or ALL_VIDEO_EXT
    records  = parse_manifest(skip_ext=skip_ext)

    # ── Delete .part files ──
    parts = scan_parts()
    if parts:
        print(info(f"Deleting {len(parts)} .part file(s)…"))
        for p in parts:
            short = str(Path(p).relative_to(BASE))
            if not dry_run:
                os.remove(p)
            print(f"  {clr('deleted', C.RED)} {short}")
    else:
        print(ok("No .part files to clean."))

    # ── Delete zero-byte files ──
    zeros = scan_zero_bytes(records)
    if zeros:
        print(info(f"Deleting {len(zeros)} zero-byte file(s)…"))
        for z in zeros:
            if not dry_run:
                os.remove(z["abs_path"])
            print(f"  {clr('deleted', C.RED)} {z['name']}")
    else:
        print(ok("No zero-byte files to clean."))

    # ── Identify what needs (re)downloading ──
    missing = scan_missing(records)
    # also add back the ones we just deleted
    if zeros:
        deleted_paths = {z["abs_path"] for z in zeros}
        for r in records:
            if r["abs_path"] in deleted_paths and r not in missing:
                missing.append(r)

    if not missing:
        print(ok("Nothing to re-download — collection is complete!"))
        return

    print(info(f"Re-downloading {len(missing)} file(s)…\n"))
    if not dry_run:
        _run_download(missing)
    else:
        for m in missing:
            print(f"  {clr('[DRY-RUN]', C.DIM)} would download: {m['name']}")

# ─── Download Engine ───────────────────────────────────────────────────────────
def _run_download(records: list[dict]):
    """
    Core download loop with:
    - Progress bar (tqdm or fallback)
    - Resume support (skip already-downloaded via state file)
    - Error logging
    """
    if not GDOWN_AVAILABLE:
        print(err("gdown is not installed. Run option 0 → Install dependencies."))
        return

    state  = load_state()
    done   = set(state.get("downloaded", []))
    failed = {}

    to_dl  = [r for r in records if r["abs_path"] not in done
              and not Path(r["abs_path"]).exists()]

    if not to_dl:
        print(ok("All files already present — nothing to download."))
        return

    total = len(to_dl)
    print(f"  {bold(str(total))} file(s) queued for download.\n")

    if TQDM_AVAILABLE:
        bar = tqdm(total=total, unit="file", colour="cyan",
                   bar_format="{l_bar}{bar:30}{r_bar}", dynamic_ncols=True)
    else:
        bar = None

    for i, rec in enumerate(to_dl, 1):
        name    = rec["name"]
        fid     = rec["id"]
        out_dir = Path(rec["abs_dir"])
        out_path= Path(rec["abs_path"])

        out_dir.mkdir(parents=True, exist_ok=True)

        if bar:
            bar.set_description(clr(name[:45], C.CYAN))
        else:
            sys.stdout.write(simple_bar(i, total, label=name[:40]))
            sys.stdout.flush()

        try:
            gdown.download(id=fid, output=str(out_path), quiet=True)
            # Validate result
            if out_path.exists() and out_path.stat().st_size > 0:
                state["downloaded"].append(str(out_path))
                if bar:
                    bar.update(1)
            else:
                # Empty file — clean up and mark failed
                if out_path.exists():
                    out_path.unlink()
                failed[str(out_path)] = "Empty file after download"
        except Exception as e:
            failed[str(out_path)] = str(e)
            if bar:
                bar.update(1)

        # Persist state every 10 files
        if i % 10 == 0:
            state["failed"] = list(failed.keys())
            save_state(state)

    if bar:
        bar.close()
    else:
        print()  # newline after simple bar

    state["failed"] = list(failed.keys())
    save_state(state)

    # ── Summary ──
    n_ok  = total - len(failed)
    print(f"\n{ok(f'{n_ok}/{total} downloaded successfully.')}")
    if failed:
        print(warn(f"{len(failed)} failed:"))
        for path, reason in list(failed.items())[:10]:
            print(f"  {clr('•', C.RED)} {Path(path).name}: {reason}")
        if len(failed) > 10:
            print(clr(f"  … and {len(failed)-10} more (see .gdrive_state.json)", C.DIM))


