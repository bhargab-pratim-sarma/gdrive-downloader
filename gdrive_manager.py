def print_menu():
    print(clr("\n── MAIN MENU ───────────────────────────────────────────", C.CYAN))
    print(clr(f"  Drive : {DRIVE_URL[:60]}…", C.DIM))
    print(clr(f"  Local : {COLLECTION}", C.DIM))
    print()
